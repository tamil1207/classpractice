package document;

public class Days {

	public static void main(String[] args) {
		String day = "sunday";
		switch (day) {
		case "monday":
			System.out.println("Today is monday");
			break;
		case "tusday":
			System.out.println("Today is tusday");
			break;
		case "wensday":
			System.out.println("Today is wensday");
			break;
		case "thusday":
			System.out.println("Today is thusday");
			break;
		case "friday":
			System.out.println("Today is friday");
			break;
		case "saterday":
			System.out.println("Today is saterday");
			break;
		case "sunday":
			System.out.println("Today is sunday");
			break;
		}

	}

}
